@router.get("")
def list_tournaments():
    """List active and recent tournaments."""
    conn = get_conn()
    rows = conn.execute("""
        SELECT t.*,
               COUNT(DISTINCT tm.id) as match_count,
               COUNT(DISTINCT CASE WHEN tm.status='done' THEN tm.id END) as done_count
        FROM tournaments t
        LEFT JOIN tournament_matches tm ON tm.tournament_id = t.id
        GROUP BY t.id
        ORDER BY t.started_at DESC
        LIMIT 10
    """).fetchall()
    conn.close()
    return [dict(r) for r in rows]


@router.get("/{tournament_id}")
def get_tournament(tournament_id: str):
    """Get full tournament bracket."""
    conn = get_conn()
    t = conn.execute("SELECT * FROM tournaments WHERE id=?", (tournament_id,)).fetchone()
    if not t:
        conn.close()
        raise HTTPException(404, "Tournament not found")

    matches = conn.execute("""
        SELECT tm.*,
               a1.name as agent1_name, a1.domain as agent1_domain,
               a1.trust_score as agent1_trust, a1.battle_wins as agent1_wins,
               a2.name as agent2_name, a2.domain as agent2_domain,
               a2.trust_score as agent2_trust, a2.battle_wins as agent2_wins,
               aw.name as winner_name
        FROM tournament_matches tm
        JOIN agents a1 ON a1.id = tm.agent1_id
        JOIN agents a2 ON a2.id = tm.agent2_id
        LEFT JOIN agents aw ON aw.id = tm.winner_id
        WHERE tm.tournament_id = ?
        ORDER BY tm.round ASC, tm.match_num ASC
    """, (tournament_id,)).fetchall()
    conn.close()

    rounds: dict = {}
    for m in matches:
        r = m["round"]
        if r not in rounds:
            rounds[r] = []
        rounds[r].append(dict(m))

    return {
        **dict(t),
        "rounds": rounds,
        "total_rounds": max((m["round"] for m in matches), default=0),
    }


